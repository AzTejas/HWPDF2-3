public class ArtithmeticOperations {
    public static void main(String[] args) {
        float a=55;
        float b=20;

        System.out.println("a + b = " + (a+b));
        System.out.println("a - b = " + (a-b));
        System.out.println("a x b = " + (a*b));
        System.out.println("a / b = " + (a/b));
        System.out.println("a % b = " + (a%b));
        System.out.println("Incrementing a = " + ++a);
        System.out.println("Decrementing b = " + --b);
    }
}
